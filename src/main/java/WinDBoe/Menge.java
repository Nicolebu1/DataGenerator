package WinDBoe;

public class Menge {
    int vid;
    int pid;
    int menge;

    public Menge(int vid, int pid, int menge) {
        this.vid = vid;
        this.pid = pid;
        this.menge = menge;
    }

    public int getVid() {
        return vid;
    }

    public int getPid() {
        return pid;
    }

    public int getMenge() {
        return menge;
    }
}
